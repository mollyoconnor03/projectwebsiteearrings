class Earring:
    def __init__(self, earring_id, name, earring_type, description, metal, imageUrl, price):
        self.earring_id = earring_id
        self.name = name
        self.earring_type = earring_type
        self.description = description
        self.metal = metal
        self.imageUrl = imageUrl
        self.price = price
